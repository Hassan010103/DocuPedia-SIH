import { Document } from '../types';

const calculateSmartScore = (doc: Omit<Document, 'smartScore'>): number => {
    let score = 0;

    // 1. Base score from priority
    switch (doc.priority) {
        case 'High': score += 50; break;
        case 'Medium': score += 25; break;
        case 'Low': score += 10; break;
    }

    // 2. Deadline urgency
    if (doc.deadline) {
        const now = new Date();
        const deadlineDate = new Date(doc.deadline);
        const diffDays = (deadlineDate.getTime() - now.getTime()) / (1000 * 60 * 60 * 24);
        if (diffDays < 0) score += 40; // Overdue
        else if (diffDays <= 7) score += 30; // Due this week
        else if (diffDays <= 30) score += 15; // Due this month
    }
    
    // 3. New status bonus
    if (doc.status === 'new') {
        score += 15;
    }

    // 4. Recency bonus
    const uploadedDate = new Date(doc.uploadedAt);
    const daysSinceUpload = (new Date().getTime() - uploadedDate.getTime()) / (1000 * 60 * 60 * 24);
    if (daysSinceUpload <= 7) score += 15;
    else if (daysSinceUpload <= 30) score += 5;

    // 5. Engagement bonus (based on timeline)
    score += Math.floor(doc.timeline.length / 2);

    // 6. Department weighting
    if (['Legal', 'Finance', 'Safety & Compliance'].includes(doc.department)) {
        score += 5;
    }
    
    // 7. Keyword bonus
    const keywords = ['contract', 'audit', 'proposal', 'strategy', 'urgent', 'agreement', 'critical', 'maintenance'];
    if (keywords.some(k => doc.name.toLowerCase().includes(k))) {
        score += 10;
    }

    return Math.min(Math.round(score), 100); // Cap score at 100
};

